using BepInEx;
using BepInEx.Logging;
using System;

namespace RaiRai.UnrestrictedPlates
{
    [BepInPlugin("com.rairai.unrestrictedplates.eft", "UnrestrictedPlates", "1.0.0")]
    public class Plugin : BaseUnityPlugin
    {
        internal static ManualLogSource Log;
        public Plugin()
        {
            Log = base.Logger;
            Log.LogInfo("Loading plugin!");

            try
            {
                new ArmorHolderComponentPatch().Enable();
            }
            catch (Exception ex)
            {
                Log.LogError(ex.ToString());
            }
            Log.LogInfo("Loaded plugin!");
        }
    }
}


